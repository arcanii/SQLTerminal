// Minimal Sparkle "Check for Updates…" menu command, following Sparkle's
// recommended SwiftUI integration. The SPUStandardUpdaterController itself is
// owned by SQLTerminalApp; this just exposes the menu item and keeps it in
// sync with whether a check can run.

import SwiftUI
import Combine
import Sparkle

/// Publishes whether Sparkle can currently start an update check, so the menu
/// item disables itself while one is already in flight.
final class CheckForUpdatesViewModel: ObservableObject {
    @Published var canCheckForUpdates = false

    init(updater: SPUUpdater) {
        updater.publisher(for: \.canCheckForUpdates)
            .assign(to: &$canCheckForUpdates)
    }
}

/// The "Check for Updates…" menu item. Drop into a `CommandGroup`.
struct CheckForUpdatesView: View {
    @ObservedObject private var viewModel: CheckForUpdatesViewModel
    private let updater: SPUUpdater

    init(updater: SPUUpdater) {
        self.updater = updater
        self.viewModel = CheckForUpdatesViewModel(updater: updater)
    }

    var body: some View {
        Button("Check for Updates…") {
            updater.checkForUpdates()
        }
        .disabled(!viewModel.canCheckForUpdates)
    }
}
